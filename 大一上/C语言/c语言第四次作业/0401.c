#include <stdio.h>
int main()
{
    int s, t;
    printf("请任意输入1个整形数据（10亿以内，位数不限）：\n");
    scanf("%d", &s);
    t = 0;
    while(s!=0)
    {
        t=s%10;
        s /= 10;
        printf("%d",t);
    }

    return 0;
}
