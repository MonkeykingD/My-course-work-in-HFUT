#include <stdio.h>
int main()
{
    float a ,b;
    printf("请输入一个收入:\n");
    scanf("%f", &a);
    if (a <= 5000)
    {
        b = 0;
    }
    else if (a <= 8000)
    {
        b = (a - 5000) * 0.03;
    }
    else if (a <= 17000)
    {
        b = 90 + (a - 8000) * 0.10;
    }
    else if (a <= 30000)
    {
        b = 990 + (a - 17000) * 0.20;
    }
    else if (a <= 40000)
    {
        b = 3590 + (a - 30000) * 0.25;
    }
    else if (a <= 60000)
    {
        b = 6090 + (a - 40000) * 0.30;
    }
    else if (a <= 85000)
    {
        b = 12090 + (a - 60000) * 0.35;
    }
    else
    {
        b = 20840 + (a - 85000) * 0.45;
    }
    printf("应交的税为：%f", b);
    return 0;
}
